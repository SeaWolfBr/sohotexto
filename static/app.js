const appState = {
  authenticated: false,
  result: null,
};

const elements = {
  authLayer: document.querySelector("#authLayer"),
  authForm: document.querySelector("#authForm"),
  authStatus: document.querySelector("#authStatus"),
  username: document.querySelector("#username"),
  password: document.querySelector("#password"),
  transcriptForm: document.querySelector("#transcriptForm"),
  videoUrl: document.querySelector("#videoUrl"),
  statusMessage: document.querySelector("#statusMessage"),
  submitButton: document.querySelector("#submitButton"),
  clearButton: document.querySelector("#clearButton"),
  logoutButton: document.querySelector("#logoutButton"),
  resultSection: document.querySelector("#resultSection"),
  resultTitle: document.querySelector("#resultTitle"),
  contextPreview: document.querySelector("#contextPreview"),
  transcriptOutput: document.querySelector("#transcriptOutput"),
  copyButton: document.querySelector("#copyButton"),
  downloadTxtButton: document.querySelector("#downloadTxtButton"),
};

async function parseJson(response) {
  const data = await response.json();

  if (!response.ok) {
    throw new Error(data.message || "Não foi possível completar a operação.");
  }

  return data;
}

function setStatus(message, tone = "default") {
  elements.statusMessage.textContent = message;
  elements.statusMessage.className =
    "status" + (tone === "error" ? " error" : tone === "good" ? " good" : "");
}

function setAuthStatus(message, tone = "default") {
  elements.authStatus.textContent = message;
  elements.authStatus.style.color =
    tone === "error" ? "#8f4a33" : tone === "good" ? "#2b6b53" : "";
}

function setAuthenticated(value) {
  appState.authenticated = value;
  elements.authLayer.classList.toggle("hidden", value);
  elements.submitButton.classList.toggle("hidden", !value);
  elements.clearButton.classList.toggle("hidden", !value);
  elements.logoutButton.classList.toggle("hidden", !value);

  if (!value) {
    elements.resultSection.classList.add("hidden");
  }
}

function downloadContent(content, fileName, mimeType) {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");

  link.href = url;
  link.download = fileName;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

function buildContextPreview(result) {
  const parts = [];

  if (result.languageName || result.languageCode) {
    let langPart = `Legendas em ${result.languageName || result.languageCode}`;
    if (result.isAutoGenerated === true) langPart += " (auto-gerada)";
    parts.push(langPart);
  }

  if (result.transcriptCount) {
    parts.push(`${result.transcriptCount} segmentos`);
  }

  return parts.join(" · ");
}

function renderResult(result) {
  appState.result = result;
  elements.resultSection.classList.remove("hidden");
  elements.resultTitle.textContent = result.title || "transcrição pronta";
  elements.transcriptOutput.value = result.text || "";
  elements.contextPreview.textContent = buildContextPreview(result);

  if (window.innerWidth < 768) {
    elements.resultSection.scrollIntoView({ behavior: "smooth", block: "start" });
  }
}

async function refreshSession() {
  try {
    const response = await fetch("/api/session", { cache: "no-store" });
    const data = await parseJson(response);
    setAuthenticated(Boolean(data.authenticated));
    setStatus(
      data.authenticated
        ? "cole a URL e clique em transcrever"
        : "entre com usuário e senha para liberar a ferramenta",
    );
  } catch {
    setAuthenticated(false);
    setStatus("não foi possível verificar a sessão", "error");
  }
}

elements.authForm.addEventListener("submit", async (event) => {
  event.preventDefault();
  setAuthStatus("verificando credenciais...");

  try {
    const response = await fetch("/api/login", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        username: elements.username.value,
        password: elements.password.value,
      }),
    });

    await parseJson(response);
    elements.password.value = "";
    setAuthStatus("");
    setAuthenticated(true);
    setStatus("acesso liberado. cole a URL e clique em transcrever", "good");
  } catch (error) {
    setAuthStatus(
      error instanceof Error ? error.message : "não foi possível validar o login",
      "error",
    );
  }
});

elements.logoutButton.addEventListener("click", async () => {
  await fetch("/api/logout", { method: "POST" });
  setAuthenticated(false);
  setStatus("sessão encerrada");
});

elements.transcriptForm.addEventListener("submit", async (event) => {
  event.preventDefault();

  const url = elements.videoUrl.value.trim();

  if (!url) {
    setStatus("cole uma URL do YouTube para continuar", "error");
    return;
  }

  elements.submitButton.disabled = true;
  setStatus("buscando legenda e limpando o texto...");

  try {
    const response = await fetch("/api/transcribe", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ url }),
    });

    const data = await parseJson(response);
    renderResult(data.result);
    setStatus("transcrição pronta", "good");
  } catch (error) {
    setStatus(
      error instanceof Error ? error.message : "não foi possível gerar a transcrição",
      "error",
    );
  } finally {
    elements.submitButton.disabled = false;
  }
});

elements.clearButton.addEventListener("click", () => {
  elements.videoUrl.value = "";
  elements.resultSection.classList.add("hidden");
  elements.transcriptOutput.value = "";
  elements.contextPreview.textContent = "";
  appState.result = null;
  setStatus("cole a URL e clique em transcrever");
});

elements.copyButton.addEventListener("click", async () => {
  if (!appState.result?.text) return;

  try {
    await navigator.clipboard.writeText(appState.result.text);
    setStatus("texto copiado para a área de transferência", "good");
  } catch {
    setStatus("não foi possível copiar automaticamente", "error");
  }
});

elements.downloadTxtButton.addEventListener("click", () => {
  if (!appState.result?.text) return;

  downloadContent(
    appState.result.text,
    appState.result.exports?.txtFileName || "transcricao.txt",
    "text/plain;charset=utf-8",
  );
});

refreshSession();
