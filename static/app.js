// ─── HELPERS ──────────────────────────────────────────────

async function parseJson(response) {
  const data = await response.json();
  if (!response.ok) {
    throw new Error(data.message || s.statusError);
  }
  return data;
}

function setStatus(message, tone = "default") {
  el.statusMessage.textContent = message;
  el.statusMessage.className =
    "status" + (tone === "error" ? " error" : tone === "good" ? " good" : "");
}

function downloadContent(content, fileName, mimeType) {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = fileName;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

function buildContextPreview(result) {
  const parts = [];
  if (result.languageName || result.languageCode) {
    let langPart = `${s.contextSubtitles} ${result.languageName || result.languageCode}`;
    if (result.isAutoGenerated === true) langPart += ` ${s.contextAutoGenerated}`;
    parts.push(langPart);
  }
  if (result.transcriptCount) {
    parts.push(`${result.transcriptCount} ${s.contextSegments}`);
  }
  return parts.join(" · ");
}

function escapeHtml(text) {
  const div = document.createElement("div");
  div.textContent = text;
  return div.innerHTML;
}

// ─── TABS ─────────────────────────────────────────────────

function switchTab(tabName) {
  appState.activeTab = tabName;

  // Update tab buttons
  el.tabBar.querySelectorAll(".tab-item").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.tab === tabName);
  });

  // Update panels
  const panels = { summary: el.panelSummary, social: el.panelSocial, ideas: el.panelIdeas, transcript: el.panelTranscript };
  Object.entries(panels).forEach(([name, panel]) => {
    panel.classList.toggle("active", name === tabName);
  });
}

el.tabBar.addEventListener("click", (e) => {
  const btn = e.target.closest(".tab-item");
  if (btn?.dataset.tab) switchTab(btn.dataset.tab);
});

// ─── RENDERERS ────────────────────────────────────────────

function renderSummary(summary) {
  el.summaryLoading.classList.add("hidden");

  let html = "";

  // Key points
  if (summary.keyPoints?.length) {
    html += `<div class="summary-section"><h3>${s.summaryKeyPoints}</h3><ul>`;
    for (const point of summary.keyPoints) {
      html += `<li>${escapeHtml(point)}</li>`;
    }
    html += "</ul></div>";
  }

  // Summary text
  if (summary.text) {
    html += `<div class="summary-section"><h3>${s.summarySummary}</h3><p class="summary-text">${escapeHtml(summary.text)}</p></div>`;
  }

  // Topics
  if (summary.topics?.length) {
    html += `<div class="summary-section"><h3>${s.summaryTopics}</h3><div class="topics-bar">`;
    for (const topic of summary.topics) {
      html += `<span class="topic-tag">${escapeHtml(topic)}</span>`;
    }
    html += "</div></div>";
  }

  el.summaryContent.innerHTML = html;
}

function renderSocialPosts(socialPosts) {
  el.socialLoading.classList.add("hidden");

  const platforms = [
    { key: "twitter", label: s.socialTwitter },
    { key: "instagram", label: s.socialInstagram },
    { key: "linkedin", label: s.socialLinkedin },
  ];

  let html = "";
  for (const { key, label } of platforms) {
    const text = socialPosts[key];
    if (!text) continue;

    html += `
      <div class="social-card">
        <div class="social-card-head">
          <span class="social-card-label">${label}</span>
          <button class="social-card-copy" data-platform="${key}" type="button">${s.socialCopy}</button>
        </div>
        <div class="social-card-text">${escapeHtml(text)}</div>
      </div>`;
  }

  el.socialContent.innerHTML = html;

  // Attach copy handlers
  el.socialContent.querySelectorAll(".social-card-copy").forEach((btn) => {
    btn.addEventListener("click", async () => {
      const platform = btn.dataset.platform;
      const text = socialPosts[platform];
      if (!text) return;

      try {
        await navigator.clipboard.writeText(text);
        btn.textContent = s.socialCopied;
        btn.classList.add("copied");
        setTimeout(() => {
          btn.textContent = s.socialCopy;
          btn.classList.remove("copied");
        }, 2000);
      } catch {
        setStatus(s.statusCopyFail, "error");
      }
    });
  });
}

function renderContentIdeas(ideas) {
  el.ideasLoading.classList.add("hidden");

  let html = "";

  // Titles
  if (ideas.titles?.length) {
    html += `<div class="ideas-section"><h3>${s.ideasTitles}</h3><ol>`;
    for (const t of ideas.titles) {
      html += `<li>${escapeHtml(t)}</li>`;
    }
    html += "</ol></div>";
  }

  // Hooks
  if (ideas.hooks?.length) {
    html += `<div class="ideas-section"><h3>${s.ideasHooks}</h3><ol>`;
    for (const h of ideas.hooks) {
      html += `<li>${escapeHtml(h)}</li>`;
    }
    html += "</ol></div>";
  }

  // Angles — schema: { format, theme, idea } or plain string (fallback)
  if (ideas.angles?.length) {
    html += `<div class="ideas-section"><h3>${s.ideasAngles}</h3><ol>`;
    for (const a of ideas.angles) {
      if (typeof a === "object" && a !== null) {
        const label = [a.format, a.theme].filter(Boolean).map(escapeHtml).join(" · ");
        const idea = escapeHtml(a.idea || "");
        html += `<li>${label ? `<strong>${label}</strong> — ` : ""}${idea}</li>`;
      } else {
        html += `<li>${escapeHtml(String(a))}</li>`;
      }
    }
    html += "</ol></div>";
  }

  el.ideasContent.innerHTML = html;
}

function renderResult(result) {
  appState.result = result;
  el.resultSection.classList.remove("hidden");
  el.resultTitle.textContent = result.title || s.resultFallbackTitle;
  el.contextPreview.textContent = buildContextPreview(result);

  // Transcript always available
  el.transcriptOutput.value = result.text || "";

  // Clear AI panels
  el.summaryContent.innerHTML = "";
  el.socialContent.innerHTML = "";
  el.ideasContent.innerHTML = "";
  el.summaryLoading.classList.add("hidden");
  el.socialLoading.classList.add("hidden");
  el.ideasLoading.classList.add("hidden");

  // AI results
  if (result.summary) {
    renderSummary(result.summary);
  } else {
    el.summaryContent.innerHTML = `<p class="summary-text" style="color:var(--muted)">${escapeHtml(locale === "pt" ? "Resumo indisponível — configure a chave de API." : "Summary unavailable — configure the API key.")}</p>`;
  }
  if (result.socialPosts) {
    renderSocialPosts(result.socialPosts);
  } else {
    el.socialContent.innerHTML = `<p class="summary-text" style="color:var(--muted)">${escapeHtml(locale === "pt" ? "Posts indisponíveis — configure a chave de API." : "Posts unavailable — configure the API key.")}</p>`;
  }
  if (result.contentIdeas) {
    renderContentIdeas(result.contentIdeas);
  } else {
    el.ideasContent.innerHTML = `<p class="summary-text" style="color:var(--muted)">${escapeHtml(locale === "pt" ? "Ideias indisponíveis — configure a chave de API." : "Ideas unavailable — configure the API key.")}</p>`;
  }

  // Switch to best available tab
  const hasAI = result.summary || result.socialPosts || result.contentIdeas;
  switchTab(hasAI ? "summary" : "transcript");

  if (window.innerWidth < 768) {
    el.resultSection.scrollIntoView({ behavior: "smooth", block: "start" });
  }
}

// ─── BUILD EXPORT TEXT ────────────────────────────────────

function buildFullExport(result) {
  const sections = [];
  const divider = "\n" + "─".repeat(50) + "\n";

  // Title
  if (result.title) {
    sections.push(`${result.title}\n${"=".repeat(result.title.length)}`);
  }

  // Summary
  if (result.summary) {
    let summaryText = s.summaryKeyPoints + "\n";
    if (result.summary.keyPoints?.length) {
      summaryText += result.summary.keyPoints.map((p) => `• ${p}`).join("\n");
    }
    summaryText += "\n\n" + s.summarySummary + "\n";
    if (result.summary.text) summaryText += result.summary.text;
    if (result.summary.topics?.length) {
      summaryText += "\n\n" + s.summaryTopics + "\n" + result.summary.topics.join(" · ");
    }
    sections.push(summaryText);
  }

  // Social posts
  if (result.socialPosts) {
    let socialText = "";
    if (result.socialPosts.twitter) socialText += s.socialTwitter + "\n" + result.socialPosts.twitter + "\n\n";
    if (result.socialPosts.instagram) socialText += s.socialInstagram + "\n" + result.socialPosts.instagram + "\n\n";
    if (result.socialPosts.linkedin) socialText += s.socialLinkedin + "\n" + result.socialPosts.linkedin;
    sections.push(socialText.trim());
  }

  // Content ideas
  if (result.contentIdeas) {
    let ideasText = "";
    if (result.contentIdeas.titles?.length) {
      ideasText += s.ideasTitles + "\n" + result.contentIdeas.titles.map((t, i) => `${i + 1}. ${t}`).join("\n") + "\n\n";
    }
    if (result.contentIdeas.hooks?.length) {
      ideasText += s.ideasHooks + "\n" + result.contentIdeas.hooks.map((h, i) => `${i + 1}. ${h}`).join("\n") + "\n\n";
    }
    if (result.contentIdeas.angles?.length) {
      ideasText += s.ideasAngles + "\n" + result.contentIdeas.angles.map((a, i) => {
        if (typeof a === "object" && a !== null) {
          const label = [a.format, a.theme].filter(Boolean).join(" · ");
          return `${i + 1}. ${label ? label + " — " : ""}${a.idea || ""}`;
        }
        return `${i + 1}. ${a}`;
      }).join("\n");
    }
    sections.push(ideasText.trim());
  }

  // Transcript
  if (result.text) {
    sections.push((locale === "pt" ? "TRANSCRIÇÃO" : "TRANSCRIPT") + "\n" + result.text);
  }

  return sections.join(divider);
}

function getActiveTabText() {
  const r = appState.result;
  if (!r) return "";

  switch (appState.activeTab) {
    case "summary": {
      const parts = [];
      if (r.summary?.keyPoints?.length) parts.push(r.summary.keyPoints.map((p) => `• ${p}`).join("\n"));
      if (r.summary?.text) parts.push(r.summary.text);
      if (r.summary?.topics?.length) parts.push(r.summary.topics.join(" · "));
      return parts.join("\n\n");
    }
    case "social": {
      const parts = [];
      if (r.socialPosts?.twitter) parts.push(s.socialTwitter + "\n" + r.socialPosts.twitter);
      if (r.socialPosts?.instagram) parts.push(s.socialInstagram + "\n" + r.socialPosts.instagram);
      if (r.socialPosts?.linkedin) parts.push(s.socialLinkedin + "\n" + r.socialPosts.linkedin);
      return parts.join("\n\n");
    }
    case "ideas": {
      const parts = [];
      if (r.contentIdeas?.titles?.length) parts.push(s.ideasTitles + "\n" + r.contentIdeas.titles.map((t, i) => `${i + 1}. ${t}`).join("\n"));
      if (r.contentIdeas?.hooks?.length) parts.push(s.ideasHooks + "\n" + r.contentIdeas.hooks.map((h, i) => `${i + 1}. ${h}`).join("\n"));
      if (r.contentIdeas?.angles?.length) parts.push(s.ideasAngles + "\n" + r.contentIdeas.angles.map((a, i) => {
        if (typeof a === "object" && a !== null) {
          const label = [a.format, a.theme].filter(Boolean).join(" · ");
          return `${i + 1}. ${label ? label + " — " : ""}${a.idea || ""}`;
        }
        return `${i + 1}. ${a}`;
      }).join("\n"));
      return parts.join("\n\n");
    }
    case "transcript":
      return r.text || "";
    default:
      return "";
  }
}

// ─── EVENTS ───────────────────────────────────────────────

el.transcriptForm.addEventListener("submit", async (event) => {
  event.preventDefault();

  const url = el.videoUrl.value.trim();
  if (!url) {
    setStatus(s.statusEmptyUrl, "error");
    return;
  }

  el.submitButton.disabled = true;
  setStatus(s.statusProcessing);

  try {
    const response = await fetch("/api/process", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ url, lang: locale }),
    });

    const data = await parseJson(response);
    renderResult(data.result);
    setStatus(s.statusReady, "good");
  } catch (error) {
    setStatus(
      error instanceof Error ? error.message : s.statusError,
      "error",
    );
  } finally {
    el.submitButton.disabled = false;
  }
});

el.clearButton.addEventListener("click", () => {
  el.videoUrl.value = "";
  el.resultSection.classList.add("hidden");
  el.transcriptOutput.value = "";
  el.contextPreview.textContent = "";
  el.summaryContent.innerHTML = "";
  el.socialContent.innerHTML = "";
  el.ideasContent.innerHTML = "";
  appState.result = null;
  switchTab("summary");
  setStatus(s.statusCleared);
});

el.copyButton.addEventListener("click", async () => {
  const text = getActiveTabText();
  if (!text) return;

  try {
    await navigator.clipboard.writeText(text);
    setStatus(s.statusCopied, "good");
  } catch {
    setStatus(s.statusCopyFail, "error");
  }
});

el.downloadTxtButton.addEventListener("click", () => {
  if (!appState.result) return;

  const fullText = buildFullExport(appState.result);
  const fileName = appState.result.exports?.txtFileName || "justext-output.txt";

  downloadContent(fullText, fileName, "text/plain;charset=utf-8");
});
