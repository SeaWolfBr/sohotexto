const locale = navigator.language?.toLowerCase().startsWith("pt") ? "pt" : "en";
const s = strings[locale];

// ─── STATE ────────────────────────────────────────────────

const appState = {
  result: null,
};

// ─── ELEMENTS ─────────────────────────────────────────────

const elements = {
  heroTitle: document.querySelector("#heroTitle"),
  heroCopy: document.querySelector("#heroCopy"),
  transcriptForm: document.querySelector("#transcriptForm"),
  videoUrl: document.querySelector("#videoUrl"),
  submitButton: document.querySelector("#submitButton"),
  clearButton: document.querySelector("#clearButton"),
  statusMessage: document.querySelector("#statusMessage"),
  resultSection: document.querySelector("#resultSection"),
  resultEyebrow: document.querySelector("#resultEyebrow"),
  resultTitle: document.querySelector("#resultTitle"),
  contextPreview: document.querySelector("#contextPreview"),
  transcriptOutput: document.querySelector("#transcriptOutput"),
  copyButton: document.querySelector("#copyButton"),
  downloadTxtButton: document.querySelector("#downloadTxtButton"),
};

// ─── APPLY LOCALE ─────────────────────────────────────────

elements.heroTitle.textContent = s.heroTitle;
elements.heroCopy.textContent = s.heroCopy;
elements.submitButton.textContent = s.submitBtn;
elements.clearButton.textContent = s.clearBtn;
elements.copyButton.textContent = s.copyBtn;
elements.downloadTxtButton.textContent = s.downloadBtn;
elements.resultEyebrow.textContent = s.resultEyebrow;
elements.statusMessage.textContent = s.statusIdle;

// ─── HELPERS ──────────────────────────────────────────────

async function parseJson(response) {
  const data = await response.json();

  if (!response.ok) {
    throw new Error(data.message || s.statusError);
  }

  return data;
}

function setStatus(message, tone = "default") {
  elements.statusMessage.textContent = message;
  elements.statusMessage.className =
    "status" + (tone === "error" ? " error" : tone === "good" ? " good" : "");
}

function downloadContent(content, fileName, mimeType) {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");

  link.href = url;
  link.download = fileName;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

function buildContextPreview(result) {
  const parts = [];

  if (result.languageName || result.languageCode) {
    let langPart = `${s.contextSubtitles} ${result.languageName || result.languageCode}`;
    if (result.isAutoGenerated === true) langPart += ` ${s.contextAutoGenerated}`;
    parts.push(langPart);
  }

  if (result.transcriptCount) {
    parts.push(`${result.transcriptCount} ${s.contextSegments}`);
  }

  return parts.join(" · ");
}

function renderResult(result) {
  appState.result = result;
  elements.resultSection.classList.remove("hidden");
  elements.resultTitle.textContent = result.title || s.resultFallbackTitle;
  elements.transcriptOutput.value = result.text || "";
  elements.contextPreview.textContent = buildContextPreview(result);

  if (window.innerWidth < 768) {
    elements.resultSection.scrollIntoView({ behavior: "smooth", block: "start" });
  }
}

// ─── EVENTS ───────────────────────────────────────────────

elements.transcriptForm.addEventListener("submit", async (event) => {
  event.preventDefault();

  const url = elements.videoUrl.value.trim();

  if (!url) {
    setStatus(s.statusEmptyUrl, "error");
    return;
  }

  elements.submitButton.disabled = true;
  setStatus(s.statusFetching);

  try {
    const response = await fetch("/api/transcribe", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ url }),
    });

    const data = await parseJson(response);
    renderResult(data.result);
    setStatus(s.statusReady, "good");
  } catch (error) {
    setStatus(
      error instanceof Error ? error.message : s.statusError,
      "error",
    );
  } finally {
    elements.submitButton.disabled = false;
  }
});

elements.clearButton.addEventListener("click", () => {
  elements.videoUrl.value = "";
  elements.resultSection.classList.add("hidden");
  elements.transcriptOutput.value = "";
  elements.contextPreview.textContent = "";
  appState.result = null;
  setStatus(s.statusCleared);
});

elements.copyButton.addEventListener("click", async () => {
  if (!appState.result?.text) return;

  try {
    await navigator.clipboard.writeText(appState.result.text);
    setStatus(s.statusCopied, "good");
  } catch {
    setStatus(s.statusCopyFail, "error");
  }
});

elements.downloadTxtButton.addEventListener("click", () => {
  if (!appState.result?.text) return;

  downloadContent(
    appState.result.text,
    appState.result.exports?.txtFileName || "transcricao.txt",
    "text/plain;charset=utf-8",
  );
});
